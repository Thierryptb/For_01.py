# Démonstration utilisation boucle 'for' dans une liste

liste = [1, 2, 3, 5, 8, 13] #Création de la liste
for elem in liste: #On inclu la boucle 'for' pour rechercher chaques éléments dans la liste ci-dessus
	print('nombre actuel :', elem)

# Opération usuelle
for elem in liste:
	result = 2 * elem + 1 #On incite donc à la boucle de multiplié par 2 chaque éléments et d'y ajouter +1
	print(result) #On écrit la variable 'résult' permettant à la boucle de l'appliquer

